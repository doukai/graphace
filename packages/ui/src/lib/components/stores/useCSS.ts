function createZIndex() {
    const z_index: Writable<number> = writable(0);
    const { subscribe, set, update } = z_index;
    return {
        subscribe,
        next: () => update(($z_index) => $z_index + 10),
        top: (n?: number | undefined) => z_classes[get(z_index) + (n || 0)],
        reset: () => set(0)
    };
}

